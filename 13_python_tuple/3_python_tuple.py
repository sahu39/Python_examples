# Negative index and slicing #
def main():
    thetuple = "Hello","Halo","guy","list",1,2
    print(thetuple[-4])
    print(thetuple[-5])
    print(thetuple[-5:-2])
    print(thetuple[-2:])
    print(thetuple[:-3])
    print(thetuple[1:-1])
    print(thetuple[-1:1])

if __name__ == "__main__":
    main()
